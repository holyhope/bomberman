package controller;

public enum MyEvent {
	/**
	 * The UP value : move up
	 */
	UP,
	
	/**
	 * The DOWN value : move down
	 */
	DOWN,
	
	/**
	 * The LEFT value : move left
	 */
	LEFT,
	
	/**
	 * The RIGHT value : move right
	 */
	RIGHT,
	
	/**
	 * The BOMB value : drop a bomb
	 */
	BOMB,
	
	/**
	 * The NONE value
	 */
	NONE,
	
	/**
	 * The SURREND value : surrender
	 */
	SURREND,
	
	/**
	 * The READY value : READY, FIGHT !
	 */
	READY
}
